/**
 * Get audio source from database path
 * @param audioPath - Path from database (e.g., 'src/audio/ubiquitous.mp3')
 * @returns Audio source or null if not found
 */
export const getAudioSource = (audioPath: string) => {
  const filename = audioPath.split('/').pop()?.replace('.mp3', '');
  return audioFiles[filename || ''] || null;
};

/**
 * Get image source from database path
 * @param imagePath - Path from database (e.g., 'src/image/ubiquitous.png')
 * @returns Image source or null if not found
 */
export const getImageSource = (imagePath: string) => {
  const filename = imagePath.split('/').pop()?.replace('.png', '');
  return imageFiles[filename || ''] || null;
};

